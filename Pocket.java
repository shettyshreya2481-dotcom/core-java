class Pocket {

    
    String type;
    String color;
    double size;

   
    Pocket() {
        type = "Shirt Pocket";
        color = "Black";
        size = 12.5;
    }

  
    Pocket(String type, String color, double size) {
        this.type = type;
        this.color = color;
        this.size = size;
    }
}